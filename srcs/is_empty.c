#include <../includes/minishell.h>

bool	is_empty(char *line)
{
	int i;

	i = 0;
	if (line != NULL)
	{
		while (line[i] != '\0')
		{
			if (line[i] > 32 && line[i] < 127)
				return (0);
			i++;
		}
		return (1);
	}
	return (1);
}
