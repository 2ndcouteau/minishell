#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*dst;

	dst = (char*)s;
	while (*dst != '\0' && *dst != c)
		dst++;
	if (*dst == '\0')
		return (NULL);
	return (dst);
}
